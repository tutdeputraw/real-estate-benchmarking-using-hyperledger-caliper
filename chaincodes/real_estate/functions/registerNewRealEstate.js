/**
 * Workload module for the benchmark round.
 */
class CreateRealEstateWorkload extends WorkloadModuleBase {
    /**
     * Initializes the workload module instance.
     */
    constructor() {
        super();
        this.txIndex = 0;
    }

    /**
     * Assemble TXs for the round.
     * @return {Promise<TxStatus[]>}
     */
    async submitTransaction() {
        this.txIndex += 1;

        const realEstateId = realEstates[this.txIndex].id;
        const ownerId = realEstates[this.txIndex].OwnerId;
        const price = realEstates[this.txIndex].price;
        const bed = realEstates[this.txIndex].bed;
        const bath = realEstates[this.txIndex].bath;
        const acreLot = realEstates[this.txIndex].acre_lot;
        const fullAddress = realEstates[this.txIndex].full_address;
        const street = realEstates[this.txIndex].street;
        const city = realEstates[this.txIndex].city;
        const state = realEstates[this.txIndex].state;
        const zipcode = realEstates[this.txIndex].zip_code;
        const houseSize = realEstates[this.txIndex].house_size;
        const isOpenToSell = realEstates[this.txIndex].isOpenToSell;

        const args = {
            contractId: 'real_estate',
            contractVersion: 'v1',
            contractFunction: 'RealEstate_RegisterNewRealEstate',
            contractArguments: [realEstateId, ownerId, price, bed, bath, acreLot, fullAddress, street, city, state, zipcode, houseSize, isOpenToSell],
            timeout: 50
        };

        await this.sutAdapter.sendRequests(args);
    }
}

/**
 * Create a new instance of the workload module.
 * @return {WorkloadModuleInterface}
 */
function createWorkloadModule() {
    return new CreateRealEstateWorkload();
}

